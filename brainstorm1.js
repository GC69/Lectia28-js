// Cite milisecunde sunt intr-un an

// 1.Cite zile sunt intr-un an
let days = 365;

// 2.Cite ore sunt in aceste zile
let hours = days * 24;

// 3.Cite minute sunt in aceste ore
let minutes = hours * 60;

// 4.Cite secunde sunt in aceste minute
let seconds = minutes * 60;

// 5.Cite milisecinde sunt in aceste secunde
let miliseconds = seconds * 1000;

console.log(hours, minutes, seconds, miliseconds);
